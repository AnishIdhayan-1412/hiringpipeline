# Report writer
# ══════════════════════════════════════════════════════════════════════════

class ReportWriter:
    """Handles all file I/O for the explainability engine.

    Writes individual JSON explanation files and the aggregate plain-text
    summary report.  All write operations are individually guarded with
    try/except so that one failure never prevents other files being written.
    """

    # ──────────────────────────────────────────────────────────────────────
    # Individual JSON
    # ──────────────────────────────────────────────────────────────────────

    def write_explanation_json(
        self,
        explanation: Dict[str, Any],
        output_dir:  str,
    ) -> bool:
        """Write one candidate's explanation dict as a formatted JSON file.

        Args:
            explanation: The assembled explanation dict from ExplanationBuilder.
            output_dir:  Directory to write the file into.

        Returns:
            True on success, False on any I/O error.
        """
        cid  = explanation["candidate_id"]
        path = os.path.join(output_dir, f"{cid}.json")
        try:
            with open(path, "w", encoding="utf-8") as fh:
                json.dump(explanation, fh, indent=2, ensure_ascii=False)
            logger.debug("Wrote explanation JSON → %s", path)
            return True
        except OSError as exc:
            logger.error("Failed to write explanation JSON for %s: %s", cid, exc)
            return False

    # ──────────────────────────────────────────────────────────────────────
    # Summary report
    # ──────────────────────────────────────────────────────────────────────

    def write_summary_report(
        self,
        explanations: List[Dict[str, Any]],
        output_dir:   str,
        jd_skill_count: int,
    ) -> bool:
        """Write the aggregate plain-text summary report for all candidates.

        The report is structured into three parts:
          1. Header — run metadata and overall statistics
          2. Ranked table — one row per candidate with key scores
          3. Per-candidate sections — full plain-English explanation

        Args:
            explanations:   List of assembled explanation dicts, sorted by rank.
            output_dir:     Directory to write summary_report.txt into.
            jd_skill_count: Number of skills extracted from the JD (for header).

        Returns:
            True on success, False on any I/O error.
        """
        lines = self._build_report_lines(explanations, jd_skill_count)
        path  = os.path.join(output_dir, SUMMARY_FILENAME)
        try:
            with open(path, "w", encoding="utf-8") as fh:
                fh.write("\n".join(lines))
            logger.info("Summary report saved → %s", path)
            return True
        except OSError as exc:
            logger.error("Failed to write summary report: %s", exc)
            return False

    def _build_report_lines(
        self,
        explanations:   List[Dict[str, Any]],
        jd_skill_count: int,
    ) -> List[str]:
        """Build the list of text lines that make up the summary report.

        Args:
            explanations:   Sorted list of explanation dicts.
            jd_skill_count: Number of JD skills (for header context).

        Returns:
            List of string lines (no trailing newline on each).
        """
        W    = REPORT_WIDTH
        sep  = "═" * W
        thin = "─" * W
        now  = datetime.now().strftime(HUMAN_DATE_FMT)

        n_candidates  = len(explanations)
        n_recommended = sum(
            1 for e in explanations
            if e["verdict"] in {"Strong Match", "Good Match"}
        )
        n_strong      = sum(1 for e in explanations if e["verdict"] == "Strong Match")
        avg_score     = (
            sum(e["final_score"] for e in explanations) / n_candidates
            if n_candidates > 0
            else 0.0
        )

        lines: List[str] = []

        # ── Header ────────────────────────────────────────────────────────
        lines += [
            sep,
            _centre("BIAS-FREE HIRING PIPELINE", W),
            _centre("CANDIDATE EXPLAINABILITY REPORT", W),
            sep,
            f"  Generated      : {now}",
            f"  Candidates     : {n_candidates}",
            f"  JD Skill Count : {jd_skill_count}",
            f"  Average Score  : {_pct(avg_score)}",
            f"  Recommended    : {n_recommended} of {n_candidates} "
            f"(Strong: {n_strong})",
            sep,
            "",
        ]

        # ── Ranked table ──────────────────────────────────────────────────
        lines.append(_centre("RANKED OVERVIEW", W))
        lines.append(thin)

        # Table header
        col_rank    = 4
        col_cand    = 22
        col_score   = 7
        col_sem     = 8
        col_kw      = 7
        col_qual    = 7
        col_verdict = 15
        col_matched = 9

        header = (
            f"  {'Rank':<{col_rank}} "
            f"{'Candidate':<{col_cand}} "
            f"{'Score':>{col_score}} "
            f"{'Semantic':>{col_sem}} "
            f"{'Keyword':>{col_kw}} "
            f"{'Quality':>{col_qual}} "
            f"{'Verdict':<{col_verdict}} "
            f"{'Skills':>{col_matched}}"
        )
        subline = (
            f"  {'-'*col_rank} "
            f"{'-'*col_cand} "
            f"{'-'*col_score} "
            f"{'-'*col_sem} "
            f"{'-'*col_kw} "
            f"{'-'*col_qual} "
            f"{'-'*col_verdict} "
            f"{'-'*col_matched}"
        )
        lines += [header, subline]

        for e in explanations:
            bd         = e["explanation"]["score_breakdown"]
            sk         = e["explanation"]["skills"]
            matched_n  = len(e["explanation"]["skills"]["matched"])
            total_n    = e["explanation"]["score_breakdown"]["keyword_score"].get(
                "note", ""  # not the right field, use jd_skill_count approach
            )
            # Recover jd_skill_count from the candidate's own data:
            # matched / match_rate
            mr_str     = sk.get("match_rate", "N/A")
            skill_str  = f"{matched_n}/{jd_skill_count}" if jd_skill_count > 0 else f"{matched_n}/??"

            row = (
                f"  {e['rank']:<{col_rank}} "
                f"{e['candidate_id']:<{col_cand}} "
                f"{_pct(e['final_score']):>{col_score}} "
                f"{_pct(bd['semantic_score']['value']):>{col_sem}} "
                f"{_pct(bd['keyword_score']['value']):>{col_kw}} "
                f"{_pct(bd['quality_score']['value']):>{col_qual}} "
                f"{e['verdict']:<{col_verdict}} "
                f"{skill_str:>{col_matched}}"
            )
            lines.append(row)

        lines += ["", thin, ""]

        # ── Per-candidate narrative ────────────────────────────────────────
        lines.append(_centre("CANDIDATE DETAIL", W))
        lines.append("")

        for e in explanations:
            cid      = e["candidate_id"]
            exp_sec  = e["explanation"]
            rank     = e["rank"]
            verdict  = e["verdict"]
            ds       = e.get("data_sources", {})

            lines += [
                thin,
                f"  #{rank}  {cid}  |  {verdict}  |  Final Score: {_pct(e['final_score'])}",
                thin,
            ]

            # Data availability notice
            if not ds.get("parsed_json_available", True):
                lines.append(
                    "  [!] Parsed CV data was unavailable for this candidate. "
                    "Explanation is based on ranking signals only."
                )
                lines.append("")

            # Headline
            lines.append(f"  {e['headline']}")
            lines.append("")

            # Score breakdown
            lines.append("  SCORE BREAKDOWN")
            bd = exp_sec["score_breakdown"]
            for key, label in [
                ("final_score",    "Final"),
                ("semantic_score", "Semantic"),
                ("keyword_score",  "Keyword"),
                ("quality_score",  "Quality"),
            ]:
                entry = bd[key]
                bar   = _mini_bar(entry["value"])
                note  = entry.get("note", "")
                lines.append(
                    f"    {label:<10}  {_pct(entry['value']):>5}  [{bar}]  {entry['label']}"
                )
                if note:
                    lines.append(f"             {_wrap_indent(note, width=W - 14, indent=13)}")
            lines.append("")

            # Skills
            sk = exp_sec["skills"]
            lines.append("  SKILLS")
            lines.append(f"    {sk['summary']}")
            if sk["matched"]:
                lines.append(
                    f"    Matched : {', '.join(sk['matched'])}"
                )
            if sk["missing"]:
                lines.append(
                    f"    Missing : {', '.join(sk['missing'])}"
                )
            lines.append("")

            # Experience & Education
            lines.append("  EXPERIENCE")
            lines.append(f"    {exp_sec['experience']['summary']}")
            lines.append("")
            lines.append("  EDUCATION")
            lines.append(f"    {exp_sec['education']['summary']}")
            lines.append("")

            # Strengths
            if exp_sec["strengths"]:
                lines.append("  STRENGTHS")
                for s in exp_sec["strengths"]:
                    for part in _wrap_bullet(s, width=W - 8, prefix="    ✓ "):
                        lines.append(part)
                lines.append("")

            # Gaps
            if exp_sec["gaps"]:
                lines.append("  GAPS")
                for g in exp_sec["gaps"]:
                    for part in _wrap_bullet(g, width=W - 8, prefix="    ✗ "):
                        lines.append(part)
                lines.append("")

            # Recommendation
            lines.append("  RECOMMENDATION")
            for part in textwrap.wrap(
                exp_sec["recommendation"],
                width=W - 6,
                initial_indent="    ",
                subsequent_indent="    ",
            ):
                lines.append(part)
            lines.append("")

        # ── Footer ────────────────────────────────────────────────────────
        lines += [
            sep,
            _centre("PIPELINE STATISTICS", W),
            sep,
            f"  Total candidates  : {n_candidates}",
            f"  Strong Match      : {sum(1 for e in explanations if e['verdict'] == 'Strong Match')}",
            f"  Good Match        : {sum(1 for e in explanations if e['verdict'] == 'Good Match')}",
            f"  Partial Match     : {sum(1 for e in explanations if e['verdict'] == 'Partial Match')}",
            f"  Weak Match        : {sum(1 for e in explanations if e['verdict'] == 'Weak Match')}",
            f"  Average score     : {_pct(avg_score)}",
            f"  Recommended       : {n_recommended} ({_pct(n_recommended / n_candidates) if n_candidates else '0%'})",
            "",
            "  This report was generated automatically by the Bias-Free Hiring",
            "  Pipeline. All candidate identifiers are anonymized (module0).",
            "  Scores reflect algorithmic signals only and must be reviewed by",
            "  a qualified human decision-maker before any hiring action is taken.",
            sep,
            _centre("END OF REPORT", W),
            sep,
            "",
        ]

        return lines


