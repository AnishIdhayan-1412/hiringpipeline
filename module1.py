# Explanation builder
# ══════════════════════════════════════════════════════════════════════════

class ExplanationBuilder:
    """Assembles the full explanation JSON structure for one candidate.

    Delegates all text generation to TextGenerator and structures the
    results according to the canonical output schema.

    Attributes:
        _gen: The TextGenerator instance used for all NL generation.
    """

    def __init__(self) -> None:
        """Initialise the builder with its text generator."""
        self._gen: TextGenerator = TextGenerator()

    def build(self, ctx: CandidateContext) -> Dict[str, Any]:
        """Build the complete explanation dict for one candidate.

        Args:
            ctx: Complete merged context for the candidate.

        Returns:
            A dict matching the canonical explanation JSON schema.
        """
        r     = ctx.ranking
        p     = ctx.parsed
        gen   = self._gen

        v              = _verdict(r.final_score)
        headline_text  = gen.headline(ctx)
        exp_months     = p.total_experience_months if p.available else 0
        level, field   = (_highest_education(p.education) if p.available else ("", ""))
        matched_count  = len(r.matched_skills)
        total_skills   = r.jd_skill_count

        # ── Score breakdown ───────────────────────────────────────────────
        score_breakdown: Dict[str, Any] = {
            "semantic_score": {
                "value": round(r.semantic_score, 4),
                "label": _score_label(r.semantic_score),
                "note":  gen.semantic_note(ctx),
            },
            "keyword_score": {
                "value": round(r.keyword_score, 4),
                "label": _score_label(r.keyword_score),
                "note":  gen.keyword_note(ctx),
            },
            "quality_score": {
                "value": round(r.quality_score, 4),
                "label": _score_label(r.quality_score),
                "note":  gen.quality_note(ctx),
            },
            "final_score": {
                "value": round(r.final_score, 4),
                "label": v,
            },
        }

        # ── Skills block ──────────────────────────────────────────────────
        match_rate = (
            _pct(r.keyword_score)
            if total_skills > 0
            else "N/A"
        )
        skills_block: Dict[str, Any] = {
            "matched":    list(r.matched_skills),
            "missing":    list(r.missing_skills),
            "match_rate": match_rate,
            "summary":    gen.skills_summary(ctx),
        }

        # ── Experience block ──────────────────────────────────────────────
        experience_block: Dict[str, Any] = {
            "total_months":      exp_months,
            "total_years":       str(round(exp_months / 12, 1)) if exp_months > 0 else "0",
            "roles_detected":    len(p.roles) if p.available else 0,
            "parse_data_available": p.available,
            "summary":           gen.experience_summary(ctx),
        }
        if not p.available:
            experience_block["note"] = "Detailed experience data unavailable — parsed CV not found."

        # ── Education block ───────────────────────────────────────────────
        education_block: Dict[str, Any] = {
            "highest_level":        level or None,
            "field":                field or None,
            "entries_detected":     len(p.education) if p.available else 0,
            "parse_data_available": p.available,
            "summary":              gen.education_summary(ctx),
        }
        if not p.available:
            education_block["note"] = "Detailed education data unavailable — parsed CV not found."

        # ── Certifications block ──────────────────────────────────────────
        certifications_block: Dict[str, Any] = {
            "count":   len(p.certifications) if p.available else 0,
            "entries": p.certifications if p.available else [],
        }

        # ── Strengths / Gaps / Recommendation ─────────────────────────────
        strengths_list     = gen.strengths(ctx)
        gaps_list          = gen.gaps(ctx)
        recommendation_txt = gen.recommendation(ctx)

        # ── Assemble ──────────────────────────────────────────────────────
        return {
            "candidate_id":  r.candidate_id,
            "rank":          r.rank,
            "n_candidates":  ctx.n_candidates,
            "final_score":   round(r.final_score, 4),
            "verdict":       v,
            "headline":      headline_text,
            "data_sources":  {
                "ranking_details_available": True,
                "parsed_json_available":     p.available,
                "parse_warnings_count":      len(p.parse_warnings) if p.available else 0,
            },
            "explanation": {
                "score_breakdown": score_breakdown,
                "skills":          skills_block,
                "experience":      experience_block,
                "education":       education_block,
                "certifications":  certifications_block,
                "strengths":       strengths_list,
                "gaps":            gaps_list,
                "recommendation":  recommendation_txt,
            },
            "generated_at": datetime.now().strftime(ISO_FMT),
        }


