# run() — primary programmatic entry point called by main.py
# ══════════════════════════════════════════════════════════════════════════

def run(
    ranking_details: Dict[str, Dict[str, Any]],
    parsed_dir:      str,
    output_dir:      str,
) -> bool:
    """Execute the full explainability stage.

    This function is the canonical entry point for programmatic callers
    (main.py).  It mirrors the ``run()`` convention established by
    module0 and module0b, providing a consistent pipeline interface.

    Args:
        ranking_details: The ``last_ranking_details`` dict from
                         module1.ScreenRanker.rank_candidates().
        parsed_dir:      Path to the directory of parsed JSON files
                         produced by module0b.
        output_dir:      Destination directory for explanation outputs.
                         Created automatically if it does not exist.

    Returns:
        True if at least one explanation file was written successfully.
        False if ranking_details is empty or all candidates failed.
    """
    import time

    print()
    print("=" * 60)
    print("  MODULE 2 — EXPLAINABILITY ENGINE")
    print("=" * 60)

    if not ranking_details:
        print("\n  [ERROR] ranking_details is empty.")
        print("          Ensure module1.run() completed successfully before this stage.")
        return False

    n = len(ranking_details)
    print(f"\n  Candidates   : {n}")
    print(f"  Parsed JSON  : {parsed_dir}")
    print(f"  Output       : {output_dir}")
    print()

    t0     = time.time()
    engine = _get_engine()
    ok     = engine.explain(
        ranking_details = ranking_details,
        parsed_dir      = parsed_dir,
        output_dir      = output_dir,
    )
    elapsed = time.time() - t0

    if ok:
        print(f"\n  [OK] Explanations generated in {elapsed:.1f}s")
        print(f"  Outputs → {output_dir}/")
        print(f"  Summary → {os.path.join(output_dir, SUMMARY_FILENAME)}")
    else:
        print("\n  [FAILED] Explainability stage could not generate any output.")
        print("           Check logs for details.")

    print("=" * 60)
    return ok


