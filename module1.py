# Formatting helpers (module-private)
# ══════════════════════════════════════════════════════════════════════════

def _centre(text: str, width: int) -> str:
    """Centre-pad a string to a given width with surrounding spaces.

    Args:
        text:  The string to centre.
        width: Total line width.

    Returns:
        Centred string.
    """
    return text.center(width)


def _mini_bar(score: float, length: int = 20) -> str:
    """Render a compact ASCII progress bar for a 0–1 score.

    Args:
        score:  Value between 0.0 and 1.0.
        length: Total number of characters in the bar.

    Returns:
        A string of filled ('█') and empty ('░') block characters.
    """
    filled = round(score * length)
    return "█" * filled + "░" * (length - filled)


def _wrap_indent(text: str, width: int, indent: int) -> str:
    """Wrap text to *width* columns with a hanging indent of *indent* spaces.

    Args:
        text:   The text to wrap.
        width:  Maximum line width.
        indent: Number of spaces on subsequent lines.

    Returns:
        The wrapped text as a single string (may contain embedded newlines).
    """
    wrapped = textwrap.fill(
        text,
        width=width,
        subsequent_indent=" " * indent,
    )
    return wrapped


def _wrap_bullet(text: str, width: int, prefix: str) -> List[str]:
    """Wrap a bullet point item, aligning continuation lines with the text.

    Args:
        text:   The bullet text (without the prefix).
        width:  Maximum line width (including the prefix).
        prefix: The bullet prefix string (e.g. '    ✓ ').

    Returns:
        List of strings; first item has the prefix, subsequent items are
        indented to align with the text start.
    """
    hanging = " " * len(prefix)
    wrapped = textwrap.wrap(
        text,
        width=width,
        initial_indent=prefix,
        subsequent_indent=hanging,
    )
    return wrapped if wrapped else [prefix + text]


