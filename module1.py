# Explainability Engine — main orchestration class
# ══════════════════════════════════════════════════════════════════════════

class ExplainabilityEngine:
    """Orchestrates the full explainability stage for all ranked candidates.

    This class is the single entry point for all explanation generation.  It
    delegates to ExplanationBuilder for per-candidate logic and to
    ReportWriter for I/O, keeping orchestration cleanly separated from
    business logic.

    Attributes:
        _builder: Shared ExplanationBuilder instance.
        _writer:  Shared ReportWriter instance.
    """

    def __init__(self) -> None:
        """Initialise the engine with its builder and writer dependencies."""
        self._builder: ExplanationBuilder = ExplanationBuilder()
        self._writer:  ReportWriter       = ReportWriter()

    def explain(
        self,
        ranking_details: Dict[str, Dict[str, Any]],
        parsed_dir:      str,
        output_dir:      str,
    ) -> bool:
        """Generate all explanations and write output artefacts.

        For each candidate in *ranking_details*:
          1. Load enrichment data from *parsed_dir* (gracefully skipped if absent).
          2. Build the full explanation dict via ExplanationBuilder.
          3. Write individual JSON to *output_dir*.

        After all candidates are processed:
          4. Write the aggregate summary_report.txt.

        Args:
            ranking_details: The ``last_ranking_details`` dict from module1's
                             ScreenRanker, keyed by candidate_id.
            parsed_dir:      Path to the directory containing module0b parsed
                             JSON files.
            output_dir:      Destination directory for all output files.

        Returns:
            True if at least one explanation was written successfully,
            False if every candidate failed or *ranking_details* is empty.
        """
        if not ranking_details:
            logger.warning("ranking_details is empty — nothing to explain.")
            return False

        try:
            os.makedirs(output_dir, exist_ok=True)
        except OSError as exc:
            logger.error("Cannot create output directory '%s': %s", output_dir, exc)
            return False

        n_candidates    = len(ranking_details)
        jd_skill_count  = self._infer_jd_skill_count(ranking_details)
        explanations:   List[Dict[str, Any]] = []
        success_count:  int = 0

        # Sort processing order by rank so log output is naturally ordered.
        sorted_entries = sorted(
            ranking_details.items(),
            key=lambda kv: kv[1].get("rank", 999),
        )

        for candidate_id, raw_data in sorted_entries:
            logger.info(
                "Processing %s (rank %s of %d) …",
                candidate_id,
                raw_data.get("rank", "?"),
                n_candidates,
            )

            try:
                ranking_entry = RankingEntry.from_dict(candidate_id, raw_data)
            except Exception as exc:
                logger.error(
                    "Failed to parse ranking data for %s: %s — skipping.",
                    candidate_id, exc,
                )
                continue

            parsed_ctx = _load_parsed_context(parsed_dir, candidate_id)

            ctx = CandidateContext(
                ranking      = ranking_entry,
                parsed       = parsed_ctx,
                n_candidates = n_candidates,
            )

            try:
                explanation = self._builder.build(ctx)
            except Exception as exc:
                logger.error(
                    "Explanation builder failed for %s: %s — skipping.",
                    candidate_id, exc,
                )
                continue

            if self._writer.write_explanation_json(explanation, output_dir):
                success_count += 1
                explanations.append(explanation)
            else:
                # Writing failed but we still keep the dict for the summary
                explanations.append(explanation)

        if not explanations:
            logger.error("No explanations could be generated or written.")
            return False

        # Sort by rank for the summary report
        explanations.sort(key=lambda e: e.get("rank", 999))

        self._writer.write_summary_report(
            explanations    = explanations,
            output_dir      = output_dir,
            jd_skill_count  = jd_skill_count,
        )

        logger.info(
            "Explainability stage complete — %d/%d explanations written.",
            success_count, n_candidates,
        )

        return success_count > 0

    @staticmethod
    def _infer_jd_skill_count(ranking_details: Dict[str, Dict[str, Any]]) -> int:
        """Infer the JD skill count from any entry in ranking_details.

        All candidates share the same JD so any entry's jd_skill_count is
        representative.

        Args:
            ranking_details: The full ranking_details dict.

        Returns:
            The JD skill count, or 0 if it cannot be determined.
        """
        for data in ranking_details.values():
            count = data.get("jd_skill_count", 0)
            if count:
                return int(count)
        return 0


