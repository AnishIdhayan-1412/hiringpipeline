# Parsed-JSON loader
# ══════════════════════════════════════════════════════════════════════════

def _load_parsed_context(parsed_dir: str, candidate_id: str) -> ParsedContext:
    """Load and validate the module0b parsed JSON for one candidate.

    Args:
        parsed_dir:   Path to the directory containing parsed JSON files.
        candidate_id: Candidate identifier (e.g. 'Candidate_01').

    Returns:
        A populated ParsedContext if the file exists and is valid,
        otherwise ParsedContext.unavailable() with a logged warning.
    """
    path = os.path.join(parsed_dir, f"{candidate_id}.json")
    if not os.path.isfile(path):
        logger.warning(
            "Parsed JSON not found for %s — explanation will be generated "
            "from ranking signals only (path: %s).",
            candidate_id, path,
        )
        return ParsedContext.unavailable()

    try:
        with open(path, "r", encoding="utf-8") as fh:
            data = json.load(fh)
    except json.JSONDecodeError as exc:
        logger.error(
            "Parsed JSON for %s is malformed: %s. "
            "Proceeding with ranking signals only.",
            candidate_id, exc,
        )
        return ParsedContext.unavailable()
    except OSError as exc:
        logger.error(
            "Could not read parsed JSON for %s: %s. "
            "Proceeding with ranking signals only.",
            candidate_id, exc,
        )
        return ParsedContext.unavailable()

    try:
        return ParsedContext.from_dict(data)
    except Exception as exc:   # guard against unexpected schema drift
        logger.error(
            "Unexpected error constructing ParsedContext for %s: %s. "
            "Proceeding with ranking signals only.",
            candidate_id, exc,
        )
        return ParsedContext.unavailable()


