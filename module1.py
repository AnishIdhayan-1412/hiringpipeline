"""
module2.py — Explainability Engine
====================================
Bias-Free Hiring Pipeline — Stage 2

Reads the ranking decisions produced by module1 and transforms raw numeric
scores into structured, human-readable explanations that a hiring manager can
act on immediately — without needing to understand the underlying ML model.

Design philosophy
-----------------
*   **Zero external dependencies.** Pure Python stdlib.  No LLMs, no ML, no
    HTTP calls.  Every sentence is generated by deterministic rule-based
    templates wired to the actual signal values.
*   **Truthfulness over polish.**  Text never flatters or obscures weak scores.
    Labels, verdicts, and recommendations are anchored to concrete thresholds.
*   **Graceful degradation.**  A missing parsed JSON never crashes the engine —
    it generates a leaner explanation from ranking_details alone and flags the
    gap clearly.
*   **Auditability.**  Every output JSON records exactly which data were
    available and which were inferred from defaults, forming a paper trail for
    GDPR audit (Stage 3).

Pipeline position
-----------------
    module0  → anonymized_cvs/Candidate_01.txt
    module0b → parsed/Candidate_01.json
    module1  → ranking_report.txt  +  ranker.last_ranking_details
    module2  → explanations/Candidate_01.json       ← one per candidate
               explanations/summary_report.txt      ← all candidates

Standalone usage
----------------
    python module2.py --ranking-file ranking_details.json \\
                      --parsed-dir parsed \\
                      --output-dir explanations

    # Dump last_ranking_details from a running pipeline for debugging:
    python -c "import json, module1; r = module1.load_ranker(); \\
               json.dump(r.last_ranking_details, open('ranking_details.json','w'), indent=2)"

Programmatic usage (called by main.py)
----------------------------------------
    import module2
    ok = module2.run(
        ranking_details=ranker.last_ranking_details,
        parsed_dir='parsed',
        output_dir='explanations',
    )
"""

