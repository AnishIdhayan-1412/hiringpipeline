# ── Verdict thresholds ────────────────────────────────────────────────────
VERDICT_STRONG_THRESHOLD:  float = 0.75
VERDICT_GOOD_THRESHOLD:    float = 0.55
VERDICT_PARTIAL_THRESHOLD: float = 0.40

# ── Score label thresholds ────────────────────────────────────────────────
LABEL_EXCELLENT_THRESHOLD: float = 0.80
LABEL_GOOD_THRESHOLD:      float = 0.60
LABEL_FAIR_THRESHOLD:      float = 0.40

# ── Strengths detection thresholds ────────────────────────────────────────
STRENGTH_KEYWORD_THRESHOLD:    float = 0.70
STRENGTH_SEMANTIC_THRESHOLD:   float = 0.70
STRENGTH_EXPERIENCE_MONTHS:    int   = 24
STRENGTH_QUALITY_THRESHOLD:    float = 0.80
STRENGTH_SENIOR_MONTHS:        int   = 60   # 5 years — triggers "senior" phrasing
STRENGTH_PERFECT_KEYWORD:      float = 1.0  # all JD skills present

# ── Gaps detection thresholds ─────────────────────────────────────────────
GAP_LIMITED_EXPERIENCE_MONTHS: int   = 12
GAP_LOW_KEYWORD_THRESHOLD:     float = 0.30
GAP_LOW_QUALITY_THRESHOLD:     float = 0.50
GAP_MAX_SKILL_ITEMS:           int   = 5    # cap individual skill gap bullets

# ── Advanced degree identifiers (for strength detection) ─────────────────
ADVANCED_DEGREE_LEVELS: frozenset = frozenset({
    "PhD", "M.Sc", "M.A", "MBA", "M.E", "M.Tech", "Master", "MCA",
    "Postgraduate",
})

# ── Formatting ────────────────────────────────────────────────────────────
REPORT_WIDTH:    int = 78   # column width of the summary report
ISO_FMT:         str = "%Y-%m-%dT%H:%M:%S"
HUMAN_DATE_FMT:  str = "%d %B %Y, %H:%M"
OUTPUT_DIR_NAME: str = "explanations"
SUMMARY_FILENAME: str = "summary_report.txt"
LOG_FORMAT:      str = "%(asctime)s [%(levelname)s] %(name)s: %(message)s"
DATE_FMT:        str = "%H:%M:%S"


