# Verdict & label helpers
# ══════════════════════════════════════════════════════════════════════════

def _verdict(score: float) -> str:
    """Map a final score to its human-readable verdict string.

    Args:
        score: The candidate's final_score (0.0 – 1.0).

    Returns:
        One of: "Strong Match", "Good Match", "Partial Match", "Weak Match".
    """
    if score >= VERDICT_STRONG_THRESHOLD:
        return "Strong Match"
    if score >= VERDICT_GOOD_THRESHOLD:
        return "Good Match"
    if score >= VERDICT_PARTIAL_THRESHOLD:
        return "Partial Match"
    return "Weak Match"


def _score_label(score: float) -> str:
    """Map a numeric score (0–1) to a qualitative label.

    Args:
        score: Any sub-score value (semantic, keyword, quality, final).

    Returns:
        One of: "Excellent", "Good", "Fair", "Poor".
    """
    if score >= LABEL_EXCELLENT_THRESHOLD:
        return "Excellent"
    if score >= LABEL_GOOD_THRESHOLD:
        return "Good"
    if score >= LABEL_FAIR_THRESHOLD:
        return "Fair"
    return "Poor"


def _pct(score: float) -> str:
    """Format a 0–1 score as a percentage string, e.g. 0.75 → '75%'.

    Args:
        score: A value between 0.0 and 1.0.

    Returns:
        Percentage string with no decimal places.
    """
    return f"{round(score * 100)}%"


def _years_months_str(total_months: int) -> str:
    """Express a duration in months as a natural English string.

    Examples:
        0  → "no recorded experience"
        6  → "6 months"
        12 → "1 year"
        18 → "1 year 6 months"
        24 → "2 years"

    Args:
        total_months: Non-negative integer number of months.

    Returns:
        A natural English duration string.
    """
    if total_months <= 0:
        return "no recorded experience"
    years  = total_months // 12
    months = total_months % 12
    if years == 0:
        return f"{months} month{'s' if months != 1 else ''}"
    if months == 0:
        return f"{years} year{'s' if years != 1 else ''}"
    return (
        f"{years} year{'s' if years != 1 else ''} "
        f"{months} month{'s' if months != 1 else ''}"
    )


def _highest_education(education: List[Dict[str, Any]]) -> Tuple[str, str]:
    """Determine the highest education level and field from an education list.

    The ordering of ``_EDU_PRIORITY`` defines which level is considered
    'highest'; the first match wins.

    Args:
        education: List of education entry dicts from module0b.

    Returns:
        Tuple of (level_string, field_string).  Both may be empty strings.
    """
    _EDU_PRIORITY: List[str] = [
        "PhD", "MBA", "M.Tech", "M.Sc", "M.E", "M.A", "MCA", "Master",
        "Postgraduate", "B.Tech", "B.E", "B.Sc", "B.A", "BCA", "Bachelor",
        "Undergraduate", "Diploma", "Certificate", "HSC", "SSLC",
        "High School", "Secondary", "CBSE", "ICSE",
    ]
    if not education:
        return "", ""

    best_level = ""
    best_field = ""
    best_priority = len(_EDU_PRIORITY) + 1   # lower index = higher degree

    for entry in education:
        level = entry.get("level", "")
        field = entry.get("field", "")
        try:
            priority = _EDU_PRIORITY.index(level)
        except ValueError:
            priority = len(_EDU_PRIORITY)

        if priority < best_priority:
            best_priority = priority
            best_level    = level
            best_field    = field

    return best_level, best_field


# ══════════════════════════════════════════════════════════════════════════
# Rule-based text generation
# ══════════════════════════════════════════════════════════════════════════

class TextGenerator:
    """Pure rule-based natural language generator for candidate explanations.

    Every public method accepts a ``CandidateContext`` and returns a string
    or list of strings.  There are no side effects, no I/O, no ML models.
    Template selection is driven by the actual signal values so that the
    output reads differently for strong vs weak candidates.
    """

    # ──────────────────────────────────────────────────────────────────────
    # Headline
    # ──────────────────────────────────────────────────────────────────────

    def headline(self, ctx: CandidateContext) -> str:
        """Generate a one-sentence headline capturing rank, verdict, and key signal.

        The headline is the first thing a hiring manager reads and must
        convey the most important finding immediately.

        Args:
            ctx: Merged candidate context.

        Returns:
            A concise, concrete one-sentence headline string.
        """
        r   = ctx.ranking
        n   = ctx.n_candidates
        v   = _verdict(r.final_score)
        pct = _pct(r.final_score)
        exp = ctx.parsed.total_experience_months

        rank_phrase = (
            f"Ranked #{r.rank} of {n}"
            if n > 1
            else f"Ranked #{r.rank}"
        )

        # Skill summary clause
        if r.jd_skill_count > 0:
            skill_clause = (
                f"{len(r.matched_skills)} of {r.jd_skill_count} required skills matched"
            )
        else:
            skill_clause = "skill overlap assessed"

        # Experience clause
        if exp >= STRENGTH_SENIOR_MONTHS:
            exp_clause = f"{exp // 12}+ years of experience"
        elif exp >= STRENGTH_EXPERIENCE_MONTHS:
            exp_clause = f"{exp // 12} years of relevant experience"
        elif exp > 0:
            exp_clause = f"{_years_months_str(exp)} of experience on record"
        else:
            exp_clause = "experience data not available from CV"

        # Compose by verdict tier
        if v == "Strong Match":
            return (
                f"{rank_phrase} — {v.lower()} ({pct}); "
                f"{skill_clause} with {exp_clause}."
            )
        if v == "Good Match":
            return (
                f"{rank_phrase} — {v.lower()} ({pct}); "
                f"{skill_clause}. "
                f"Solid candidate with {exp_clause}."
            )
        if v == "Partial Match":
            missing_count = len(r.missing_skills)
            gap_note = (
                f"{missing_count} skill gap{'s' if missing_count != 1 else ''} identified"
                if missing_count > 0
                else "some alignment gaps noted"
            )
            return (
                f"{rank_phrase} — {v.lower()} ({pct}); "
                f"{gap_note}. Manual review recommended."
            )
        # Weak Match
        return (
            f"{rank_phrase} — {v.lower()} ({pct}); "
            f"significant gaps relative to the job requirements."
        )

    # ──────────────────────────────────────────────────────────────────────
    # Score breakdown notes
    # ──────────────────────────────────────────────────────────────────────

    def semantic_note(self, ctx: CandidateContext) -> str:
        """Generate a contextual note for the semantic similarity score.

        Args:
            ctx: Merged candidate context.

        Returns:
            A one-sentence interpretation of the semantic score.
        """
        s = ctx.ranking.semantic_score
        if s >= 0.85:
            return "CV content is an exceptionally close match to the job description language."
        if s >= LABEL_EXCELLENT_THRESHOLD:
            return "CV content closely mirrors the themes and terminology of the job description."
        if s >= LABEL_GOOD_THRESHOLD:
            return "CV content shows good topical overlap with the job description."
        if s >= LABEL_FAIR_THRESHOLD:
            return (
                "CV content partially overlaps with the job description; "
                "some areas of the role may not be addressed."
            )
        return (
            "CV content shows limited alignment with the job description; "
            "the candidate's background may be in a different domain."
        )

    def keyword_note(self, ctx: CandidateContext) -> str:
        """Generate a contextual note for the keyword match score.

        Args:
            ctx: Merged candidate context.

        Returns:
            A one-sentence interpretation of the keyword score.
        """
        r       = ctx.ranking
        matched = len(r.matched_skills)
        total   = r.jd_skill_count

        if total == 0:
            return "No specific skills were extracted from the job description."

        if matched == total:
            return f"All {total} required skills are present in the candidate's profile."
        if r.keyword_score >= 0.80:
            return (
                f"Strong skill coverage — {matched} of {total} required skills matched, "
                f"with only minor gaps."
            )
        if r.keyword_score >= LABEL_GOOD_THRESHOLD:
            return f"Good skill coverage — {matched} of {total} required skills matched."
        if r.keyword_score >= LABEL_FAIR_THRESHOLD:
            return (
                f"Partial skill coverage — {matched} of {total} required skills matched; "
                f"notable gaps remain."
            )
        return (
            f"Limited skill overlap — only {matched} of {total} required skills found; "
            f"significant upskilling may be needed."
        )

    def quality_note(self, ctx: CandidateContext) -> str:
        """Generate a contextual note for the CV quality score.

        Args:
            ctx: Merged candidate context.

        Returns:
            A one-sentence interpretation of the quality score.
        """
        q = ctx.ranking.quality_score
        if q >= 0.90:
            return (
                "CV is exceptionally well-structured with all key sections "
                "present and detailed."
            )
        if q >= LABEL_EXCELLENT_THRESHOLD:
            return "CV is well-structured and comprehensive, covering all major sections."
        if q >= LABEL_GOOD_THRESHOLD:
            return "CV is reasonably complete; most key sections are present."
        if q >= LABEL_FAIR_THRESHOLD:
            return (
                "CV has adequate structure but some sections are sparse or missing."
            )
        return (
            "CV appears incomplete — several key sections are absent or "
            "contain minimal information."
        )

    # ──────────────────────────────────────────────────────────────────────
    # Skills summary
    # ──────────────────────────────────────────────────────────────────────

    def skills_summary(self, ctx: CandidateContext) -> str:
        """Generate a plain-English skills summary sentence.

        Args:
            ctx: Merged candidate context.

        Returns:
            A concise summary of skill match status.
        """
        r       = ctx.ranking
        matched = len(r.matched_skills)
        total   = r.jd_skill_count
        missing = list(r.missing_skills)

        if total == 0:
            return "No specific skills were extracted from the job description to compare against."

        if matched == total:
            return (
                f"Candidate demonstrates all {total} skills identified in the job description."
            )

        missing_preview = missing[:3]
        suffix = (
            f", {len(missing) - 3} more"
            if len(missing) > 3
            else ""
        )
        missing_str = (
            ", ".join(missing_preview) + suffix
            if missing_preview
            else "none"
        )

        return (
            f"Candidate has {matched} of {total} required skill{'s' if total != 1 else ''}. "
            f"Missing: {missing_str}."
        )

    # ──────────────────────────────────────────────────────────────────────
    # Experience summary
    # ──────────────────────────────────────────────────────────────────────

    def experience_summary(self, ctx: CandidateContext) -> str:
        """Generate a plain-English experience summary sentence.

        Args:
            ctx: Merged candidate context.

        Returns:
            A natural summary of the candidate's experience level.
        """
        p     = ctx.parsed
        total = p.total_experience_months

        if not p.available:
            return "Detailed experience data is unavailable (parsed CV not found)."

        if total <= 0:
            if not p.roles:
                return "No experience entries could be extracted from this CV."
            return "Experience section is present but duration data could not be determined."

        role_count  = len(p.roles)
        dur_str     = _years_months_str(total)
        role_phrase = f"across {role_count} role{'s' if role_count != 1 else ''}"

        if total >= STRENGTH_SENIOR_MONTHS:
            return (
                f"Experienced professional with {dur_str} on record {role_phrase}."
            )
        if total >= STRENGTH_EXPERIENCE_MONTHS:
            return (
                f"{dur_str.capitalize()} of professional experience recorded {role_phrase}."
            )
        if total >= 6:
            return (
                f"{dur_str.capitalize()} of experience recorded {role_phrase}; "
                f"relatively early-career profile."
            )
        return (
            f"Only {dur_str} of experience recorded. "
            f"Candidate appears to be in the early stages of their career."
        )

    # ──────────────────────────────────────────────────────────────────────
    # Education summary
    # ──────────────────────────────────────────────────────────────────────

    def education_summary(self, ctx: CandidateContext) -> str:
        """Generate a plain-English education summary sentence.

        Args:
            ctx: Merged candidate context.

        Returns:
            A natural summary of the candidate's highest qualification.
        """
        if not ctx.parsed.available:
            return "Education data is unavailable (parsed CV not found)."
        if not ctx.parsed.education:
            return "No education entries could be extracted from this CV."

        level, field = _highest_education(ctx.parsed.education)

        if level and field:
            return f"{level} in {field}."
        if level:
            return f"Highest recorded qualification: {level}."
        if field:
            return f"Degree in {field} (level not specified)."
        return "Education section present but qualification details could not be determined."

    # ──────────────────────────────────────────────────────────────────────
    # Strengths detection
    # ──────────────────────────────────────────────────────────────────────

    def strengths(self, ctx: CandidateContext) -> List[str]:
        """Generate a list of concrete strength statements for this candidate.

        Each statement is triggered by a specific threshold crossing so that
        every bullet is factually defensible — not generic praise.

        Args:
            ctx: Merged candidate context.

        Returns:
            A list of strength strings (may be empty for very weak candidates).
        """
        r          = ctx.ranking
        p          = ctx.parsed
        result: List[str] = []

        # ── Complete skill match ──────────────────────────────────────────
        if r.jd_skill_count > 0 and len(r.missing_skills) == 0:
            result.append(
                f"Complete skill match — all {r.jd_skill_count} required skills are present."
            )
        elif r.keyword_score >= STRENGTH_KEYWORD_THRESHOLD:
            pct_str = _pct(r.keyword_score)
            result.append(
                f"Strong skill alignment — {pct_str} of required skills matched "
                f"({len(r.matched_skills)} of {r.jd_skill_count})."
            )

        # ── Semantic alignment ────────────────────────────────────────────
        if r.semantic_score >= STRENGTH_SEMANTIC_THRESHOLD:
            result.append(
                "High semantic relevance — the CV's content language closely "
                "reflects the job description's requirements and domain."
            )

        # ── Experience depth ──────────────────────────────────────────────
        if p.available and p.total_experience_months >= STRENGTH_SENIOR_MONTHS:
            yrs = p.total_experience_months // 12
            result.append(
                f"Seasoned professional with over {yrs} years of experience, "
                f"suggesting deep domain knowledge."
            )
        elif p.available and p.total_experience_months >= STRENGTH_EXPERIENCE_MONTHS:
            dur = _years_months_str(p.total_experience_months)
            result.append(
                f"Solid professional track record — {dur} of experience on record."
            )

        # ── CV quality ────────────────────────────────────────────────────
        if r.quality_score >= STRENGTH_QUALITY_THRESHOLD:
            result.append(
                "Well-structured, comprehensive CV — indicates strong attention "
                "to detail and professional communication."
            )

        # ── Advanced education ────────────────────────────────────────────
        if p.available and p.education:
            level, _ = _highest_education(p.education)
            if level in ADVANCED_DEGREE_LEVELS:
                result.append(
                    f"Advanced academic qualification ({level}) — demonstrates "
                    f"strong theoretical foundation."
                )

        # ── Certifications ────────────────────────────────────────────────
        if p.available and p.certifications:
            n_certs = len(p.certifications)
            cert_label = (
                p.certifications[0]
                if n_certs == 1
                else f"{n_certs} professional certifications"
            )
            result.append(
                f"Holds {cert_label}, reflecting continued professional development."
            )

        # ── Multilingual ──────────────────────────────────────────────────
        if p.available and len(p.languages) >= 2:
            lang_str = " and ".join(p.languages[:2])
            result.append(
                f"Multilingual proficiency ({lang_str}"
                + (f" and {len(p.languages) - 2} more" if len(p.languages) > 2 else "")
                + ") — an asset for diverse team environments."
            )

        return result

    # ──────────────────────────────────────────────────────────────────────
    # Gaps detection
    # ──────────────────────────────────────────────────────────────────────

    def gaps(self, ctx: CandidateContext) -> List[str]:
        """Generate a list of concrete gap statements for this candidate.

        Gaps are presented constructively — each bullet identifies a specific
        deficiency without language that could introduce bias.

        Args:
            ctx: Merged candidate context.

        Returns:
            A list of gap strings (empty for very strong candidates).
        """
        r          = ctx.ranking
        p          = ctx.parsed
        result: List[str] = []

        # ── Individual missing skills (capped) ────────────────────────────
        missing = list(r.missing_skills)
        shown   = missing[:GAP_MAX_SKILL_ITEMS]
        for skill in shown:
            result.append(
                f"Missing '{skill}' — identified as a required skill in the job description."
            )
        overflow = len(missing) - GAP_MAX_SKILL_ITEMS
        if overflow > 0:
            result.append(
                f"…and {overflow} additional required skill{'s' if overflow != 1 else ''} "
                f"not found in this CV."
            )

        # ── Low overall keyword coverage ──────────────────────────────────
        # Only add if we didn't already enumerate individual skills above
        if not missing and r.keyword_score < GAP_LOW_KEYWORD_THRESHOLD:
            result.append(
                f"Low overall skill overlap with the job description "
                f"({_pct(r.keyword_score)} match rate). "
                f"This may indicate a domain mismatch."
            )

        # ── Limited experience ────────────────────────────────────────────
        if p.available and 0 < p.total_experience_months < GAP_LIMITED_EXPERIENCE_MONTHS:
            dur = _years_months_str(p.total_experience_months)
            result.append(
                f"Limited professional experience on record ({dur}). "
                f"Role may require a more established track record."
            )
        elif p.available and p.total_experience_months == 0 and not p.roles:
            result.append(
                "No professional experience entries could be extracted from this CV."
            )

        # ── CV quality concern ────────────────────────────────────────────
        if r.quality_score < GAP_LOW_QUALITY_THRESHOLD:
            result.append(
                f"CV completeness score is low ({_pct(r.quality_score)}). "
                f"Key sections may be absent or underspecified — "
                f"candidate may benefit from being asked to resubmit a fuller CV."
            )

        return result

    # ──────────────────────────────────────────────────────────────────────
    # Recommendation
    # ──────────────────────────────────────────────────────────────────────

    def recommendation(self, ctx: CandidateContext) -> str:
        """Generate a concrete, actionable recommendation for the hiring manager.

        The recommendation is calibrated to the verdict tier and references
        specific missing skills, experience level, and quality signals.

        Args:
            ctx: Merged candidate context.

        Returns:
            A 1–3 sentence recommendation string.
        """
        r       = ctx.ranking
        p       = ctx.parsed
        v       = _verdict(r.final_score)
        missing = list(r.missing_skills)
        top_gap = missing[0] if missing else None
        top3    = ", ".join(missing[:3]) if missing else None

        # ── Build experience context phrase ───────────────────────────────
        if p.available and p.total_experience_months >= STRENGTH_EXPERIENCE_MONTHS:
            exp_note = (
                f" The candidate's {_years_months_str(p.total_experience_months)} "
                f"of experience is a positive signal."
            )
        elif p.available and 0 < p.total_experience_months < STRENGTH_EXPERIENCE_MONTHS:
            exp_note = (
                f" Note the relatively limited experience "
                f"({_years_months_str(p.total_experience_months)})."
            )
        else:
            exp_note = ""

        # ── Compose by verdict ────────────────────────────────────────────
        if v == "Strong Match":
            if not missing:
                return (
                    "Recommended for interview — candidate meets all evaluated criteria. "
                    "Proceed to the technical assessment stage."
                    + exp_note
                )
            probe = f"Consider probing depth of knowledge in {top_gap} during the interview."
            return (
                f"Recommended for interview.{exp_note} "
                f"Strong overall fit for this role. {probe}"
            )

        if v == "Good Match":
            if not missing:
                return (
                    "Consider for interview. Good overall alignment with the role."
                    + exp_note
                    + " No skill gaps identified — verify domain depth during screening."
                )
            gaps_str = ", ".join(missing[:2])
            return (
                f"Consider for interview.{exp_note} "
                f"Good overall fit; however, the candidate may need support with "
                f"{gaps_str}. "
                f"Clarify these areas during the screening call."
            )

        if v == "Partial Match":
            if top3:
                return (
                    f"Manual review recommended.{exp_note} "
                    f"The candidate shows potential but has notable gaps in: {top3}. "
                    f"Proceed only if these skills can be developed on the job or "
                    f"if other compensating factors are present."
                )
            return (
                "Manual review recommended. Some alignment gaps are noted. "
                "Consider whether the candidate's profile fits an alternative role."
                + exp_note
            )

        # Weak Match
        if top3:
            return (
                f"Not recommended at this stage. "
                f"Significant skill gaps: {top3}."
                + exp_note
                + " Candidate may be better suited to a more junior position "
                + "or a different specialisation."
            )
        return (
            "Not recommended at this stage. "
            "The candidate's profile does not adequately match the job requirements."
            + exp_note
        )


