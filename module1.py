@dataclasses.dataclass(frozen=True)
class RankingEntry:
    """Immutable snapshot of one candidate's ranking signal from module1.

    All fields correspond directly to keys in ranker.last_ranking_details.
    Defaults guard against missing keys so callers never need try/except.
    """
    candidate_id:    str
    final_score:     float
    semantic_score:  float
    keyword_score:   float
    quality_score:   float
    matched_skills:  Tuple[str, ...]
    missing_skills:  Tuple[str, ...]
    jd_skill_count:  int
    rank:            int

    @classmethod
    def from_dict(cls, candidate_id: str, data: Dict[str, Any]) -> "RankingEntry":
        """Construct a RankingEntry from a raw ranking_details sub-dict.

        Args:
            candidate_id: The candidate's identifier string.
            data:         The raw dict from ranker.last_ranking_details.

        Returns:
            A populated, validated RankingEntry instance.
        """
        return cls(
            candidate_id   = candidate_id,
            final_score    = float(data.get("final_score",    0.0)),
            semantic_score = float(data.get("semantic_score", 0.0)),
            keyword_score  = float(data.get("keyword_score",  0.0)),
            quality_score  = float(data.get("quality_score",  0.5)),
            matched_skills = tuple(data.get("matched_skills", [])),
            missing_skills = tuple(data.get("missing_skills", [])),
            jd_skill_count = int(data.get("jd_skill_count",   0)),
            rank           = int(data.get("rank",              0)),
        )


@dataclasses.dataclass
class ParsedContext:
    """Optional enrichment data loaded from module0b's parsed JSON.

    All fields have safe defaults so downstream code never needs to None-check.
    The ``available`` flag tracks whether a real parsed file was found; it is
    recorded in the output JSON for auditability.
    """
    available:                bool             = False
    total_experience_months:  int              = 0
    roles:                    List[Dict[str, Any]] = dataclasses.field(default_factory=list)
    education:                List[Dict[str, Any]] = dataclasses.field(default_factory=list)
    certifications:           List[str]        = dataclasses.field(default_factory=list)
    languages:                List[str]        = dataclasses.field(default_factory=list)
    parse_warnings:           List[str]        = dataclasses.field(default_factory=list)

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "ParsedContext":
        """Construct a ParsedContext from a module0b parsed JSON dict.

        Args:
            data: Full parsed JSON dict (top-level, not just 'sections').

        Returns:
            A populated ParsedContext with available=True.
        """
        sections = data.get("sections", {})
        return cls(
            available               = True,
            total_experience_months = int(sections.get("total_experience_months", 0)),
            roles                   = list(sections.get("experience", [])),
            education               = list(sections.get("education",  [])),
            certifications          = list(sections.get("certifications", [])),
            languages               = list(sections.get("languages",  [])),
            parse_warnings          = list(data.get("parse_warnings", [])),
        )

    @classmethod
    def unavailable(cls) -> "ParsedContext":
        """Return an empty ParsedContext representing a missing parsed file.

        Returns:
            A ParsedContext with available=False and all fields at defaults.
        """
        return cls(available=False)


@dataclasses.dataclass(frozen=True)
class CandidateContext:
    """Complete merged view of one candidate's data from all pipeline stages.

    Combines the numeric signals from module1 (RankingEntry) with the
    structural data from module0b (ParsedContext) into a single object that
    all downstream functions operate on.
    """
    ranking:  RankingEntry
    parsed:   ParsedContext
    n_candidates: int   # total candidates in this ranking run

    @property
    def candidate_id(self) -> str:
        """Convenience accessor for the candidate's identifier."""
        return self.ranking.candidate_id


